

const dia = '5'  // 0 : Domingo

switch (dia) {
    case 0:
        console.log('Domingo')
        break;
    case 1 :
        console.log('Lunes')
        break
    case 2:
        console.log('Martes')
        break
    default:
        console.log('No es Lunes, Martes ni Domingo')
        break;
}

//   Switch  compara que sea el mismo tipo de dato. 